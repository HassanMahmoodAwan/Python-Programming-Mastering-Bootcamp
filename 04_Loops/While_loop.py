# Simple While Loop
count = 0
while (count < 3): 
    count = count + 1
    print("Hello Geek") 


# Loop with Else Statment.
count = 0
while (count < 3): 
    count = count + 1
    print("Hello Geek") 
else: 
    print("In Else Block") 


# Infinite Loop             'Never use this Loop as never ending'
while True: 
    print("Hello Geek") 